package uk.ac.ebi.pride.tools.jmzreader;

@SuppressWarnings("serial")
public class JMzReaderException extends Exception {
	public JMzReaderException() {
		super();
	}

	public JMzReaderException(String arg0) {
		super(arg0);
	}

	public JMzReaderException(Throwable arg0) {
		super(arg0);
	}

	public JMzReaderException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

}

package uk.ac.ebi.pride.tools.mzxml_parser;

public class MzXMLParsingException extends Exception {
	private static final long serialVersionUID = 1L;

	public MzXMLParsingException() {
		
	}

	public MzXMLParsingException(String arg0) {
		super(arg0);
	}

	public MzXMLParsingException(Throwable arg0) {
		super(arg0);
	}

	public MzXMLParsingException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

}
